package seagullstudio.patapania;


public class Circle {
    int x;
    int y;
    int color;
    int time;
    int speed;

    public Circle(int x, int y, int color, int time, int speed) {
        this.x = x;
        this.y = y;
        this.color = color;
        this.time = time;
        this.speed = speed;
    }
}

